    // In Rust, like in many languages, functions are an essential tool to
    // maintain clean and readable code. One can probably see the basic syntax
    // from "main"! All function defintions start with "fn", followed by the
    // name of the function, arguments (if any), a return type (if any), and
    // finally a pair of curly braces to enclose the function implementation

    // Here's a simple example to add two floats. Note two things, the type
    // anotations of the arguements and the "->" arrow operator that preced the
    // return type.
